public class numhalfpyramid {
    public static void main(String args[]){
        System.out.print("enter the value of n:");
        int n = 10;
        //outer loop
        for(int i=1;i<=n;i++){
            for(int j=1;j<=i;j++){
                System.out.print(j);
            }
            System.out.println();
            
        }
    }

    
}
